`timescale 1ns / 1ps
//////////////////////////////////////////////////////////////////////////////////
// Module Name: shift_rows
// Description: AES ShiftRows - 행 순환 이동 (FIPS 197 Section 5.1.2)
//
// state 바이트 배치 (MSB 기준):
//   행0: b0  b4  b8  b12   [127:120] [95:88]  [63:56]  [31:24]
//   행1: b1  b5  b9  b13   [119:112] [87:80]  [55:48]  [23:16]
//   행2: b2  b6  b10 b14   [111:104] [79:72]  [47:40]  [15:8]
//   행3: b3  b7  b11 b15   [103:96]  [71:64]  [39:32]  [7:0]
//
// ShiftRows:
//   행0: 이동 없음          b0   b4   b8   b12
//   행1: 1칸 왼쪽 순환      b5   b9   b13  b1
//   행2: 2칸 왼쪽 순환      b10  b14  b2   b6
//   행3: 3칸 왼쪽 순환      b15  b3   b7   b11
//////////////////////////////////////////////////////////////////////////////////

module shift_rows(
    input  wire [127:0] in,
    output wire [127:0] out
);

    // 바이트 추출 (MSB 기준)
    wire [7:0] b0  = in[127:120]; wire [7:0] b1  = in[119:112];
    wire [7:0] b2  = in[111:104]; wire [7:0] b3  = in[103:96];
    wire [7:0] b4  = in[95:88];   wire [7:0] b5  = in[87:80];
    wire [7:0] b6  = in[79:72];   wire [7:0] b7  = in[71:64];
    wire [7:0] b8  = in[63:56];   wire [7:0] b9  = in[55:48];
    wire [7:0] b10 = in[47:40];   wire [7:0] b11 = in[39:32];
    wire [7:0] b12 = in[31:24];   wire [7:0] b13 = in[23:16];
    wire [7:0] b14 = in[15:8];    wire [7:0] b15 = in[7:0];

    // ShiftRows 결과 조합 (열 우선 column-major 유지)
    assign out = {
        b0,  b5,  b10, b15,   // 열0: 행0그대로, 행1+1, 행2+2, 행3+3
        b4,  b9,  b14, b3,    // 열1
        b8,  b13, b2,  b7,    // 열2
        b12, b1,  b6,  b11    // 열3
    };

endmodule
