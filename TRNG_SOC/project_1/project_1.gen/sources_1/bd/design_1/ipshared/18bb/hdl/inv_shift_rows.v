`timescale 1ns / 1ps
//////////////////////////////////////////////////////////////////////////////////
// Module Name: inv_shift_rows
// Description: AES InvShiftRows - 역 행 이동 (FIPS 197 Section 5.3.1)
//              순수 조합회로 (클록 없음)
//
// AES 상태 행렬 (열 우선 저장, 128비트 빅엔디안):
//   byte idx:  b0  b4  b8  b12   ← 행 0 (이동 없음)
//              b1  b5  b9  b13   ← 행 1 (오른쪽 1 이동)
//              b2  b6  b10 b14   ← 행 2 (오른쪽 2 이동 = 왼쪽 2)
//              b3  b7  b11 b15   ← 행 3 (오른쪽 3 이동 = 왼쪽 1)
//
// 출력 행렬:
//   b0  b4  b8  b12
//   b13 b1  b5  b9
//   b10 b14 b2  b6
//   b7  b11 b15 b3
//
// 비트 매핑: byte N = in[127 - N*8 -: 8]
//   b0 =in[127:120]  b4 =in[95:88]   b8 =in[63:56]   b12=in[31:24]
//   b1 =in[119:112]  b5 =in[87:80]   b9 =in[55:48]   b13=in[23:16]
//   b2 =in[111:104]  b6 =in[79:72]   b10=in[47:40]   b14=in[15:8]
//   b3 =in[103:96]   b7 =in[71:64]   b11=in[39:32]   b15=in[7:0]
//////////////////////////////////////////////////////////////////////////////////

module inv_shift_rows(
    input  wire [127:0] in,
    output wire [127:0] out
);

    // 열 우선 출력 (Col0 | Col1 | Col2 | Col3)
    assign out = {
        // Col 0: s'[0,0] s'[1,0] s'[2,0] s'[3,0] = b0, b13, b10, b7
        in[127:120], in[23:16],  in[47:40],  in[71:64],
        // Col 1: s'[0,1] s'[1,1] s'[2,1] s'[3,1] = b4, b1, b14, b11
        in[95:88],   in[119:112], in[15:8],  in[39:32],
        // Col 2: s'[0,2] s'[1,2] s'[2,2] s'[3,2] = b8, b5, b2, b15
        in[63:56],   in[87:80],  in[111:104], in[7:0],
        // Col 3: s'[0,3] s'[1,3] s'[2,3] s'[3,3] = b12, b9, b6, b3
        in[31:24],   in[55:48],  in[79:72],  in[103:96]
    };

endmodule
